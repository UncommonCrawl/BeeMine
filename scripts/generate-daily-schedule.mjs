import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

import { levelCatalog } from "../words.js";
import { mapDefinitions } from "../mapLayouts.js";

const MIN_WORD_LENGTH = 6;
const SCHEDULE_DAYS = 90;

function getPlayableWordLetters(word) {
  return String(word || "")
    .toUpperCase()
    .replace(/[^A-Z]/g, "");
}

function getLocalDateKey(date) {
  const year = date.getFullYear();
  const month = String(date.getMonth() + 1).padStart(2, "0");
  const day = String(date.getDate()).padStart(2, "0");
  return `${year}-${month}-${day}`;
}

function addDays(date, days) {
  const next = new Date(date);
  next.setDate(next.getDate() + days);
  return next;
}

function shuffle(array) {
  const copy = [...array];
  for (let i = copy.length - 1; i > 0; i -= 1) {
    const j = Math.floor(Math.random() * (i + 1));
    [copy[i], copy[j]] = [copy[j], copy[i]];
  }
  return copy;
}

function chooseRandom(array) {
  const index = Math.floor(Math.random() * array.length);
  return array[index];
}

function buildPools() {
  const pools = new Map();
  Object.entries(levelCatalog || {}).forEach(([category, words]) => {
    if (!Array.isArray(words)) return;
    const cleanedWords = words
      .map((word) => String(word || "").toUpperCase())
      .filter((word) => getPlayableWordLetters(word).length >= MIN_WORD_LENGTH);
    if (cleanedWords.length === 0) return;
    pools.set(category, shuffle(cleanedWords));
  });
  return pools;
}

function buildSchedule(days = SCHEDULE_DAYS) {
  const startDate = new Date();
  const pools = buildPools();
  const mapIds = mapDefinitions.map((entry) => entry.id).filter(Boolean);
  const schedule = [];
  let previousCategory = null;
  let previousMapId = null;

  if (mapIds.length === 0) {
    throw new Error("Unable to build schedule: no maps available.");
  }

  for (let offset = 0; offset < days; offset += 1) {
    const categoriesWithWords = [...pools.entries()]
      .filter(([, words]) => words.length > 0)
      .map(([category]) => category);

    if (categoriesWithWords.length === 0) {
      throw new Error("Unable to build full schedule: ran out of eligible words.");
    }

    const preferredCategories = categoriesWithWords.filter((category) => category !== previousCategory);
    const categoryChoices = preferredCategories.length > 0 ? preferredCategories : categoriesWithWords;
    const chosenCategory = chooseRandom(categoryChoices);
    const wordPool = pools.get(chosenCategory);
    const chosenWord = wordPool.pop();
    const preferredMapIds = mapIds.filter((mapId) => mapId !== previousMapId);
    const mapChoices = preferredMapIds.length > 0 ? preferredMapIds : mapIds;
    const chosenMapId = chooseRandom(mapChoices);

    const date = addDays(startDate, offset);
    schedule.push({
      date: getLocalDateKey(date),
      category: chosenCategory,
      word: chosenWord,
      mapId: chosenMapId,
    });

    previousCategory = chosenCategory;
    previousMapId = chosenMapId;
  }

  return {
    generatedAt: new Date().toISOString(),
    startDate: schedule[0]?.date || null,
    days,
    entries: schedule,
  };
}

function writeScheduleFile(schedule) {
  const fileText = `// This file is auto-generated by scripts/generate-daily-schedule.mjs\nexport const dailyScheduleMeta = ${JSON.stringify(
    {
      generatedAt: schedule.generatedAt,
      startDate: schedule.startDate,
      days: schedule.days,
    },
    null,
    2
  )};\n\nexport const dailySchedule = ${JSON.stringify(schedule.entries, null, 2)};\n`;

  const __filename = fileURLToPath(import.meta.url);
  const __dirname = path.dirname(__filename);
  const outPath = path.resolve(__dirname, "../dailySchedule.js");
  fs.writeFileSync(outPath, fileText);
  return outPath;
}

const schedule = buildSchedule();
const outPath = writeScheduleFile(schedule);
console.log(`Wrote ${schedule.entries.length} daily entries to ${outPath}`);
