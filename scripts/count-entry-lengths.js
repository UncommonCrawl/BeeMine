const fs = require('node:fs');
const path = require('node:path');
const vm = require('node:vm');

const repoRoot = process.cwd();
const wordsPath = path.join(repoRoot, 'words.js');
const outputPath = path.join(repoRoot, 'entry-length-counts.js');

function parseLevelCatalog(sourceText) {
  const marker = 'export const levelCatalog =';
  if (!sourceText.includes(marker)) {
    throw new Error('Could not find `export const levelCatalog =` in words.js');
  }

  const transformed = `${sourceText.replace(marker, 'const levelCatalog =')}\nmodule.exports = { levelCatalog };`;
  const context = {
    module: { exports: {} },
    exports: {},
  };

  vm.runInNewContext(transformed, context, { filename: 'words.js' });
  return context.module.exports.levelCatalog;
}

function incrementCount(map, key) {
  map.set(key, (map.get(key) || 0) + 1);
}

function printHistogram(title, map) {
  console.log(title);
  const rows = [...map.entries()].sort((a, b) => a[0] - b[0]);
  for (const [lengthValue, count] of rows) {
    console.log(`${lengthValue}: ${count}`);
  }
  console.log('');
}

function printAlphaCounts(title, counts) {
  console.log(title);
  for (const letter of 'ABCDEFGHIJKLMNOPQRSTUVWXYZ') {
    console.log(`${letter}: ${counts[letter]}`);
  }
  console.log('');
}

function numericMapToObjectLiteral(map) {
  const entries = [...map.entries()].sort((a, b) => a[0] - b[0]);
  const lines = ['{'];
  for (const [key, count] of entries) {
    lines.push(`  ${JSON.stringify(String(key))}: ${count},`);
  }
  lines.push('}');
  return lines.join('\n');
}

const wordsSource = fs.readFileSync(wordsPath, 'utf8');
const levelCatalog = parseLevelCatalog(wordsSource);

const lettersHistogram = new Map();
const phraseStartsByLetter = Object.fromEntries(
  [...'ABCDEFGHIJKLMNOPQRSTUVWXYZ'].map((letter) => [letter, 0]),
);

for (const words of Object.values(levelCatalog)) {
  if (!Array.isArray(words)) {
    continue;
  }

  for (const entry of words) {
    if (typeof entry !== 'string') {
      continue;
    }

    const letterCount = (entry.match(/[A-Za-z]/g) || []).length;
    const firstAlphaChar = (entry.match(/[A-Za-z]/) || [null])[0];

    incrementCount(lettersHistogram, letterCount);
    if (firstAlphaChar) {
      const letter = firstAlphaChar.toUpperCase();
      phraseStartsByLetter[letter] += 1;
    }
  }
}

printHistogram('Entries By Letter Count', lettersHistogram);
printAlphaCounts('Phrases Beginning By Letter', phraseStartsByLetter);

const alphaCountLines = [...'ABCDEFGHIJKLMNOPQRSTUVWXYZ']
  .map((letter) => `  ${JSON.stringify(letter)}: ${phraseStartsByLetter[letter]},`);

const outputLines = [
  '// Auto-generated by scripts/count-entry-lengths.js',
  '',
  'export const entriesByLetterCount = ' + numericMapToObjectLiteral(lettersHistogram) + ';',
  '',
  'export const phraseStartsByLetter = {',
  ...alphaCountLines,
  '};',
  '',
];

fs.writeFileSync(outputPath, outputLines.join('\n'), 'utf8');
console.log(`Wrote ${outputPath}`);
